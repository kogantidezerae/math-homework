#include <stdio.h>

void main() {
    int num1 = 5;
    int num2 = 3;

    printf("Addition: %d + %d = %d\n", num1, num2, (num1 + num2));

    printf("Subtraction: %d - %d = %d\n", num1, num2, (num1 - num2));

    printf("Multiplication: %d * %d = %d\n", num1, num2, (num1 * num2));

    printf("Division: %d / %d = %d\n", num1, num2, (num1 / num2));
}
